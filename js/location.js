var m = document.getElementById('map');
        var p = document.getElementById('mapM');
        var n = document.getElementById('mapImg')
        function mapFunc() {
            if (m.style.display === 'none') {
                m.style.display = 'block';
                n.style.display = 'none';
            }
        }

